function A333_setECAMfailureSystemPage()

	local systemPage = 0
	local checkZone2 = true

	for _, message in ipairs(A333_ewd_msg_cue_L) do
		if A333_ewd_msg[message.Name].Monitor.video.OUT == 1 					-- ONLY SET THE SYS PAGE IF THE WARNING IS NOT CLEARED
			and A333_ewd_msg[message.Name].isVisible							-- AND THE WARNING IS BEING DISPLAYED
		then
			systemPage = A333_ewd_msg[message.Name].SysPage
			checkZone2 = false
			break
		end
	end

	if checkZone2 then
		for _, message in ipairs(A333_ewd_msg_cue_R) do
			if A333_ewd_msg[message.Name].Monitor.video.OUT == 1 					-- ONLY SET THE SYS PAGE IF THE WARNING IS NOT CLEARED
				and A333_ewd_msg[message.Name].isVisible							-- AND THE WARNING IS BEING DISPLAYED
			then
				systemPage = A333_ewd_msg[message.Name].SysPage
				break
			end
		end
	end

	A333_SetFailureSystemPage(systemPage)

end


function A333_SetFailureSystemPage(systemPage)
	if systemPage ~= A333_ecam_failure_system_page_num then						-- ONLY SET IF SYSTEM FAILURE PAGE HAS CHANGED
		A333_resetManualSystemPageData()										-- CANX AND INITIALIZE THE MANUAL SYS PAGE LOGIC
		A333_resetManualStatusPageData()										-- CANX AND INITIALIZE THE MANUAL STATUS PAGE LOGIC
		A333_ecam_failure_system_page_num = systemPage
	end
end








--*************************************************************************************--
--** 				                   PROCESSING             	     	  			 **--
--*************************************************************************************--

function A333_fws_430()

	A333_setECAMfailureSystemPage()

end


