function A333_fws_sts_msg_cue_L()

	local sts_msg_cue_L = {}

	for _, msg in pairs(A333_sts_msg) do
		if msg.Zone == 0 then
			if msg.Video.OUT == 1 then
				table.insert(sts_msg_cue_L, {Name = msg.Name, Zone = msg.Zone, Type = msg.Type, Sequence = msg.Sequence})
			end
		end
	end

	sort_on_values_ascending(sts_msg_cue_L, 'Sequence')

	A333_sts_msg_cue_L = {}
	A333_sts_msg_cue_L = deepTableCopyT(sts_msg_cue_L, seen)

end





function A333_fws_sts_msg_cue_R()

	local sts_msg_cue_R = {}

	for _, msg in pairs(A333_sts_msg) do
		if msg.Zone == 1 then
			if msg.Video.OUT == 1 then
				table.insert(sts_msg_cue_R, {Name = msg.Name, Zone = msg.Zone, Type = msg.Type, Sequence = msg.Sequence})
			end
		end
	end

	sort_on_values_ascending(sts_msg_cue_R, 'Sequence')

	A333_sts_msg_cue_R = {}
	A333_sts_msg_cue_R = deepTableCopyT(sts_msg_cue_R, seen)

	-- NOTE: A333_sts_msg_cue_R DOES NOT CURRENTLY INCLUDE MAINTENANCE MESSAGES

end








--*************************************************************************************--
--** 				                   PROCESSING             	     	  			 **--
--*************************************************************************************--

function A333_fws_610()

	A333_fws_sts_msg_cue_L()
	A333_fws_sts_msg_cue_R()

end


