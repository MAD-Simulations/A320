function A333_fws_sts_monitor()

	for _, msg in pairs(A333_sts_msg) do

		local output
		if msg.Video.IN == 0 then
			output = 0


		elseif msg.Video.IN == 1 then

			if msg.Inhibit[A333DR_flight_phase] == 0 then
				output = 1
			end

		end



		if output == 1 then

			if msg.Video.OUT == 2 then   								-- (CLR) THIS WARNING WAS CLEARED BY THE CLR PUSHBUTTON
				output = 2
			end

		end

		msg.Video.OUT = output

	end

end












